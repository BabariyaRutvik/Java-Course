public class for_each 
{
    public static void main(String[] args) {
        // Example: Iterating over an array of integers using a for-each loop
        System.out.println("--- Iterating over an Integer Array ---");
        int[] numbers = {10, 20, 30, 40, 50};
        for (int num : numbers) {
            System.out.println("Number: " + num);
        }
    }
}


