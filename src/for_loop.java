public class for_loop 
{
    public static void main(String[] args){
        // Example: Printing numbers from 1 to 5 using a for loop
        System.out.println("--- Counting from 1 to 5 ---");
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count: " + i);
        }
    }
}

